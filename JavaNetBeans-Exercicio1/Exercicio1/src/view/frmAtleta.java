    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
      ArrayList<Atleta> ListaAt = atl.Mostrar();
      dados.setNumRows(0);
      for (Atleta a: ListaAt)
      {
          dados.addRow(new Object[]{ a.getNome(), a.getIdade()});
      }
      tblAtleta.setModel(dados);
//JOptionPane.showMessageDialog(this, "Aletas Cadastrados:" + atl.Mostrar());

    }//GEN-LAST:event_btnMostrarActionPerformed

